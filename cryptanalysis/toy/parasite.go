package toy

import (
	"github.com/OpenWhiteBox/primitives/number"
)

// parasite represents the input or output mask of a single byte going into/out of an obfuscated affine layer.
type parasite struct {
	frobenius int
	scalar    number.ByteFieldElem
}

func (p *parasite) Encode(in byte) byte {
	temp := number.ByteFieldElem(in)
	for i := 0; i < p.frobenius; i++ {
		temp = temp.Mul(temp)
	}

	return byte(temp.Mul(p.scalar))
}

func (p *parasite) Decode(in byte) byte {
	temp := number.ByteFieldElem(in).Mul(p.scalar.Invert())

	n := (8 - p.frobenius) % 8
	for i := 0; i < n; i++ {
		temp = temp.Mul(temp)
	}

	return byte(temp)
}

// Convert returns the parasite which corresponds to p on the other side of an S-box layer.
func (p *parasite) Convert() *parasite {
	frob := (8 - p.frobenius) % 8
	scalar := p.scalar

	for i := 0; i < frob; i++ {
		scalar = scalar.Mul(scalar)
	}

	return &parasite{frob, scalar}
}

